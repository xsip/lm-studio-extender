import { ApiProperty } from '@nestjs/swagger';
import { Equals, IsIn, IsOptional } from 'class-validator';

export class ToolSearchCallDto {
  /** The arguments supplied to the tool search call. */
  @ApiProperty({ description: `The arguments supplied to the tool search call.` })
  arguments!: unknown;

  /** The item type. Always `tool_search_call`. */
  @ApiProperty({
    description: `The item type. Always \`tool_search_call\`.`,
    type: 'string',
    enum: ['tool_search_call'],
  })
  @Equals('tool_search_call')
  type!: 'tool_search_call';

  /** The unique ID of this tool search call. */
  @ApiProperty({
    required: false,
    description: `The unique ID of this tool search call.`,
    type: 'string',
  })
  @IsOptional()
  id?: null | string;

  /** The unique ID of the tool search call generated by the model. */
  @ApiProperty({
    required: false,
    description: `The unique ID of the tool search call generated by the model.`,
    type: 'string',
  })
  @IsOptional()
  call_id?: null | string;

  /** Whether tool search was executed by the server or by the client. */
  @ApiProperty({
    required: false,
    description: `Whether tool search was executed by the server or by the client.`,
    enum: ['server', 'client'],
  })
  @IsOptional()
  @IsIn(['server', 'client'])
  execution?: 'server' | 'client';

  /** The status of the tool search call. */
  @ApiProperty({
    required: false,
    description: `The status of the tool search call.`,
    enum: ['in_progress', 'completed', 'incomplete'],
  })
  @IsOptional()
  @IsIn(['in_progress', 'completed', 'incomplete'])
  status?: null | 'in_progress' | 'completed' | 'incomplete';
}
